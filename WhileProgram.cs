// While Loop Program // while(condition) {code to be executed.}

using System;  
public class InfiniteForProgram
    {  
        
      public static void Main(string[] args)  
      {  
          int a = 1;

          while (a <= 15 )  
          {  
                Console.WriteLine(a);  
                a++;
          }    
      }  
 } 