// DoWhile Loop Program do { // code to be executed} while(condition);

using System;  
public class InfiniteForProgram
    {  
        
      public static void Main(string[] args)  
      {  
          int a = 1;
          do  
          {  
              Console.WriteLine(a);
              a++;
          }   
          while(a <= 15);
      }  
 } 