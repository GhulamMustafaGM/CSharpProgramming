// TypeConversion

using System;

namespace TypeConversionApp
{
    class StringConversion
    {
        static void Main(string[] args)
        {
            int i = 76;
            float f = 52.005f;
            double d = 2445.7652;
            bool b = true;

            Console.WriteLine(i.ToString());
            Console.WriteLine(f.ToString());
            Console.WriteLine(d.ToString());
            Console.WriteLine(b.ToString());
            Console.ReadKey();
            Console.ReadLine();

        }

    }
}